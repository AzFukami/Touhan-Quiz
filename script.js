const quizContainerElement = document.getElementById('quiz-container');
const quizElement = document.getElementById('quiz');
const questionNumberElement = document.getElementById('question-number');
const questionElement = document.getElementById('question');
const optionsElement = document.getElementById('options');
const resultElement = document.getElementById('result');
const resultMessageElement = document.getElementById('result-message');
const correctAnswerElement = document.getElementById('correct-answer');
const explanationElement = document.getElementById('explanation');
const prevButton = document.getElementById('prev-button');
const nextButton = document.getElementById('next-button');
const endMessageElement = document.getElementById('end-message');
const restartButton = document.getElementById('restart-button');
const backToTopButton = document.getElementById('back-to-top-button');
const overallResultElement = document.getElementById('overall-result');
const passFailResultElement = document.getElementById('pass-fail-result');
const categoryResultsElement = document.getElementById('category-results');
const chapterButtons = document.querySelectorAll('.chapter-button');
const quizBackToTopButton = document.getElementById('quiz-back-to-top'); // ← 追加！

// --- クイズの状態 (前回と同じ) ---
let currentQuizQuestions = [];
let currentQuestionIndex = 0;
let currentShuffledOptions = [];
let currentCorrectAnswerString = "";

// --- 合格基準など (CHAPTER_NAMES を正式名称に更新！) ---
const PASS_THRESHOLD_TOTAL = 70;
const PASS_THRESHOLD_CHAPTER = 40;
const CHAPTER_NAMES = { // ← 正式名称に更新！
    1: "第1章：医薬品に共通する特性と基本的な知識",
    2: "第2章：人体の働きと医薬品",
    3: "第3章：主な医薬品とその作用",
    4: "第4章：薬事に関する法規と制度",
    5: "第5章：医薬品の適正使用と安全対策"
};

// --- 配列シャッフル関数 (前回と同じ) ---
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// --- 章選択ボタンの処理 (前回と同じ) ---
chapterButtons.forEach(button => {
    button.addEventListener('click', () => {
        const selectedChapter = button.dataset.chapter;
        startQuiz(selectedChapter);
    });
});

// --- クイズの初期化・開始 (前回と同じ) ---
function startQuiz(selectedChapter) {
    startScreenElement.classList.add('hide');
    endMessageElement.classList.add('hide');
    quizContainerElement.classList.remove('hide');
    quizElement.classList.remove('hide');
    resultElement.classList.add('hide');

    let filteredQuestions;
    if (selectedChapter === 'all') {
        filteredQuestions = quizData;
         // 全問モードの場合、章タイトル表示用に chapter: 'all' のような情報を持たせるか検討
         // →今回は問題オブジェクトのchapterをそのまま使うので不要
    } else {
        const chapterNum = parseInt(selectedChapter);
        filteredQuestions = quizData.filter(q => q.chapter === chapterNum);
    }

    filteredQuestions.forEach(q => {
        q.userAnswer = null;
        q.isCorrect = null;
        q.answeredOptionIndex = null;
    });

    currentQuizQuestions = shuffleArray([...filteredQuestions]);
    currentQuestionIndex = 0;

    // クイズデータがない場合の処理を追加
    if (currentQuizQuestions.length === 0) {
        alert(selectedChapter === 'all' ? 'クイズデータがありません！' : `第${selectedChapter}章のクイズデータがありません！`);
        backToTop(); // 表紙に戻る
        return;
    }

    showQuestion();
}

// --- 問題と選択肢を表示 (前回と同じ、CHAPTER_NAMES参照箇所は自動で更新される) ---
function showQuestion() {
    resultElement.classList.add('hide');
    const currentQuestion = currentQuizQuestions[currentQuestionIndex];

    // 章タイトル表示が正式名称になる！
    questionNumberElement.textContent = `(${currentQuestionIndex + 1} / ${currentQuizQuestions.length}) ${CHAPTER_NAMES[currentQuestion.chapter] || '全問題'} - 問${currentQuestion.questionNumber}`;
    questionElement.textContent = currentQuestion.question;

    currentCorrectAnswerString = currentQuestion.options[currentQuestion.answer];
    currentShuffledOptions = shuffleArray([...currentQuestion.options]);

    optionsElement.innerHTML = '';
    const isAnswered = currentQuestion.userAnswer !== null;

    currentShuffledOptions.forEach((option, index) => {
        const button = document.createElement('button');
        button.textContent = `${index + 1}. ${option}`;
        if (isAnswered) {
            button.disabled = true;
        } else {
            button.addEventListener('click', () => selectAnswer(option, index, button));
        }
        optionsElement.appendChild(button);
    });

    if (isAnswered) {
        showResult(currentQuestion.answeredOptionIndex, true);
    }

    updateNavigationButtons();
}


// --- 回答を選択した時の処理 (前回と同じ) ---
function selectAnswer(selectedOptionString, selectedIndex, selectedButton) {
    const currentQuestion = currentQuizQuestions[currentQuestionIndex];
    const isCorrect = selectedOptionString === currentCorrectAnswerString;
    currentQuestion.userAnswer = selectedOptionString;
    currentQuestion.isCorrect = isCorrect;
    currentQuestion.answeredOptionIndex = selectedIndex;
    showResult(selectedIndex, false);
    updateNavigationButtons();
}

// --- 結果を表示する関数 (前回と同じ) ---
function showResult(selectedIndex, isRedisplay) {
    const currentQuestion = currentQuizQuestions[currentQuestionIndex];
    const optionButtons = optionsElement.querySelectorAll('button');

    optionButtons.forEach(button => {
        button.disabled = true;
        button.classList.remove('correct-option', 'incorrect-option', 'selected-incorrect');
    });

    resultElement.classList.remove('hide');

    const correctAnswerIndexAfterShuffle = currentShuffledOptions.findIndex(option => option === currentCorrectAnswerString);
    const selectedButton = optionButtons[selectedIndex];

    if (currentQuestion.isCorrect) {
        resultMessageElement.textContent = isRedisplay ? '正解した問題です⭕' : '正解！やったね！😆⭕';
        resultElement.className = 'result correct';
        selectedButton?.classList.add('correct-option');
    } else {
        resultMessageElement.textContent = isRedisplay ? '不正解だった問題です❌' : '残念！不正解だよ😢❌';
        resultElement.className = 'result incorrect';
        selectedButton?.classList.add('selected-incorrect');
        optionButtons[correctAnswerIndexAfterShuffle]?.classList.add('correct-option');
    }

    correctAnswerElement.textContent = `正解: ${correctAnswerIndexAfterShuffle + 1}. ${currentCorrectAnswerString}`;
    explanationElement.textContent = `【解説】\n${currentQuestion.explanation}`;
}

// --- ナビゲーションボタンの表示・非表示を更新 (前回と同じ) ---
function updateNavigationButtons() {
    prevButton.disabled = currentQuestionIndex === 0;
    prevButton.classList.remove('hide');

    const currentQuestion = currentQuizQuestions[currentQuestionIndex];
    if (currentQuestion.userAnswer !== null) {
        if (currentQuestionIndex < currentQuizQuestions.length - 1) {
            nextButton.textContent = '次の問題へ';
        } else {
            nextButton.textContent = '結果を見る';
        }
        nextButton.classList.remove('hide');
    } else {
        nextButton.classList.add('hide');
    }
    // クイズ中の「表紙に戻る」ボタンは常に表示
    quizBackToTopButton.classList.remove('hide');
}

// --- 前の問題へ進む (前回と同じ) ---
function prevQuestion() {
    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        showQuestion();
    }
}

// --- 次の問題へ進む / 結果表示へ (前回と同じ) ---
function nextQuestion() {
     if (currentQuestionIndex < currentQuizQuestions.length - 1) {
        currentQuestionIndex++;
        showQuestion();
    } else {
        calculateAndShowFinalResults();
    }
}


// --- 最終結果の計算と表示 (前回と同じ、CHAPTER_NAMES参照箇所は自動で更新される) ---
function calculateAndShowFinalResults() {
    quizContainerElement.classList.add('hide');
    endMessageElement.classList.remove('hide');

    let totalCorrect = 0;
    const categoryResults = {};
    let answeredQuestionsInQuiz = 0;

    currentQuizQuestions.forEach(q => {
        if (q.isCorrect !== null) {
            answeredQuestionsInQuiz++;
            const chapter = q.chapter;
            if (!categoryResults[chapter]) {
                categoryResults[chapter] = { total: 0, correct: 0 };
            }
            categoryResults[chapter].total++;
            if (q.isCorrect === true) {
                totalCorrect++;
                categoryResults[chapter].correct++;
            }
        }
    });

    const totalQuestionsInQuiz = currentQuizQuestions.length;
    const totalPercentage = answeredQuestionsInQuiz > 0 ? (totalCorrect / answeredQuestionsInQuiz * 100) : 0;

    overallResultElement.textContent = `総合結果: ${totalCorrect} / ${answeredQuestionsInQuiz} 問正解 (正答率: ${totalPercentage.toFixed(1)}%)`;

    let categoryTableHTML = `
        <table>
            <thead>
                <tr>
                    <th>分野 (章)</th>
                    <th>出題数</th>
                    <th>正解数</th>
                    <th>正答率 (%)</th>
                </tr>
            </thead>
            <tbody>
    `;
    let allChaptersPass = true;
    let chaptersPresentInQuiz = new Set(currentQuizQuestions.map(q => q.chapter));

    // クイズに含まれる章のみループして表示（章タイトルは正式名称になる）
    for (const chapterNum of chaptersPresentInQuiz) {
         const chapterData = categoryResults[chapterNum] || { total: 0, correct: 0 };
         const chapterPercentage = chapterData.total > 0 ? (chapterData.correct / chapterData.total * 100) : 0;
         const chapterName = CHAPTER_NAMES[chapterNum] || `第${chapterNum}章`; // 正式名称が使われる
         const isChapterPass = chapterPercentage >= PASS_THRESHOLD_CHAPTER;
         if (chapterData.total > 0 && !isChapterPass) {
             allChaptersPass = false;
         }
         categoryTableHTML += `
             <tr>
                 <td>${chapterName}</td>
                 <td>${chapterData.total}</td>
                 <td>${chapterData.correct}</td>
                 <td class="${isChapterPass ? 'pass' : 'fail'}">${chapterPercentage.toFixed(1)}</td>
             </tr>
         `;
    }
    categoryTableHTML += `</tbody></table>`;
    categoryResultsElement.innerHTML = categoryTableHTML;

    const isOverallPass = totalPercentage >= PASS_THRESHOLD_TOTAL;
    if (answeredQuestionsInQuiz > 0 && isOverallPass && allChaptersPass) {
        passFailResultElement.textContent = "合格 (目安) です！おめでとう！🎉💯";
        passFailResultElement.className = 'pass';
    } else if (answeredQuestionsInQuiz > 0) {
         let reason = [];
         if (!isOverallPass) reason.push(`全体の正答率 (${totalPercentage.toFixed(1)}%) が${PASS_THRESHOLD_TOTAL}%未満`);
         for (const chapterNum of chaptersPresentInQuiz) {
             const chapterData = categoryResults[chapterNum] || { total: 0, correct: 0 };
             const chapterPercentage = chapterData.total > 0 ? (chapterData.correct / chapterData.total * 100) : 0;
             if (chapterData.total > 0 && chapterPercentage < PASS_THRESHOLD_CHAPTER) {
                 reason.push(`${CHAPTER_NAMES[chapterNum] || `第${chapterNum}章`}の正答率 (${chapterPercentage.toFixed(1)}%) が${PASS_THRESHOLD_CHAPTER}%未満`);
             }
         }
         if(reason.length === 0 && !isOverallPass) reason.push(`全体の正答率が基準未満`)
         if(reason.length === 0) reason.push(`合格基準未達`)

        passFailResultElement.textContent = `不合格 (目安) です...😢 (${reason.join('、')})`;
        passFailResultElement.className = 'fail';
    } else {
         passFailResultElement.textContent = "採点対象の問題がありませんでした。";
         passFailResultElement.className = '';
    }
}

// --- 章選択画面に戻る (前回と同じ) ---
function backToTop() {
    endMessageElement.classList.add('hide');
    quizContainerElement.classList.add('hide');
    startScreenElement.classList.remove('hide');
}


// --- イベントリスナーの設定 (quizBackToTopButton を追加) ---
prevButton.addEventListener('click', prevQuestion);
nextButton.addEventListener('click', nextQuestion);
restartButton.addEventListener('click', () => {
    const currentChapter = currentQuizQuestions[0]?.chapter;
    let chapterArg = 'all';
    // chapter が undefined でないことを確認
    if (typeof currentChapter !== 'undefined' && currentQuizQuestions.length < quizData.length) {
        chapterArg = currentChapter.toString();
    }
    startQuiz(chapterArg);
});
backToTopButton.addEventListener('click', backToTop);
quizBackToTopButton.addEventListener('click', backToTop); // ← 追加！クイズ中の戻るボタン

